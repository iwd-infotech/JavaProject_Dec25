package basic;

public class DataTypesConcept {  // class body

	public static void main(String[] args) { // method body
		
//		1. int
//		int - datatype, age - variable, 25 - value
		int age;
		age = 25;
//		int age = 25;  - Duplicate variables are not allowed in java
		System.out.println(age);
		int m = 100;
		System.out.println(m);
		int n = -50;
		
//		2. Double
		double d = 33.33;
		System.out.println(d);
		double bill = 1000;
		System.out.println(bill);
		
//		3. char
		char c = 'A';
		System.out.println(c);
		char c1 = '$';
		
//		4. boolean 
//		What is true & false in java? -> 
//		true & false are the keywords in java but we can use as value as well.
		boolean b = true;
		boolean b1 = false;
		
		System.out.println(b1);
		
//		5. String - String is a class in java also we can say sequence of char.
		String s = "Java";
		String s1 = "1234";
		System.out.println(s1);
		String s2 = "!@#$%";
		
//		Emp Details
		int empID = 102;
		String empName = "Ajay";
		double empSalary = 50000.0;
		boolean isPermanent = true;
		char empGrade = 'B';

		
	}

}

